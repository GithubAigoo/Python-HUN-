a = 5
b = -10
c = 0

print(a)  # eredmény: 5
print(b)  # eredmény: -10
print(c)  # eredmény: 0

# Egész számokkal végzett műveletek
sum = a + b
difference = a - b
product = a * b
quotient = a // b  # egész osztás
remainder = a % b  # maradékos osztás
power = a ** b  # hatványozás

print(sum)  # eredmény: -5
print(difference)  # eredmény: 15
print(product)  # eredmény: -50
print(quotient)  # eredmény: -1
print(remainder)  # eredmény: 5
print(power)  # eredmény: 9765625
